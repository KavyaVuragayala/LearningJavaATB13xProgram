package ex_17_Arrays;

public class Lab_070_Array_PredefinedSize {
    public static void main(String[] args) {

        int[] array = {10,20,34,21,89};
        System.out.println(array[0]);
        System.out.println(array[1]);
        System.out.println(array[2]);
        System.out.println(array.length);

        boolean[] is_boolean = {true,false,true};
        System.out.println(is_boolean.length);
        System.out.println(is_boolean[2]);
    }
}
