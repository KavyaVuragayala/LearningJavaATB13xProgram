public class Lab_054_User_Defined_Function1 {
    public static void main(String[] args) {

        function1(); // calling the function;
        function2();
    }

    static void function1(){
        System.out.println("Function 1 declared in function defination");  // prints but not returns anything
    }

    static void function2(){
        System.out.println("Function 2 declared in function defination");  // prints but not returns anything
    }
}
