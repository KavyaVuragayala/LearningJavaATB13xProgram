package ex_04_Operators;

public class Lab_014_ArthmeticOperators {
    public static void main(String[] args) {

        int a= 1;
        int b=2;    // = is a assignment operator
        float c= 2.5f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/c);
        System.out.println(a%c); // modulus operator returns remainder
    }
}

