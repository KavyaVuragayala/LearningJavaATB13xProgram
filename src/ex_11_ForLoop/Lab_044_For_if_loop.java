package ex_11_ForLoop;

public class Lab_044_For_if_loop {
    public static void main(String[] args){

        for(int i=0;i<20 ; i++)
        {
            if(i>18)
            {
                System.out.println("Eligibility for voting");
            }
            else
            {
                System.out.println("Not Eligible");
            }
        }

    }
}
